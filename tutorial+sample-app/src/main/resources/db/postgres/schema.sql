create schema if not exists petclinic;

CREATE TABLE IF NOT EXISTS petclinic.vets (
  id         INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  first_name TEXT,
  last_name  TEXT
);
CREATE INDEX IF NOT EXISTS vets_last_name ON petclinic.vets (last_name);

CREATE TABLE IF NOT EXISTS petclinic.specialties (
  id   INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT
);
CREATE INDEX IF NOT EXISTS specialties_name ON petclinic.specialties (name);

CREATE TABLE IF NOT EXISTS petclinic.vet_specialties (
  vet_id       INT NOT NULL REFERENCES petclinic.vets (id),
  specialty_id INT NOT NULL REFERENCES petclinic.specialties (id),
  UNIQUE (vet_id, specialty_id)
);

CREATE TABLE IF NOT EXISTS petclinic.types (
  id   INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT,
  UNIQUE (name)
);
CREATE INDEX IF NOT EXISTS types_name ON petclinic.types (name);

CREATE TABLE IF NOT EXISTS petclinic.owners (
  id         INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  first_name TEXT,
  last_name  TEXT,
  address    TEXT,
  city       TEXT,
  telephone  TEXT
);
CREATE INDEX IF NOT EXISTS owners_last_name ON petclinic.owners (last_name);

CREATE TABLE IF NOT EXISTS petclinic.pets (
  id         INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name       TEXT,
  birth_date DATE,
  type_id    INT NOT NULL REFERENCES petclinic.types (id),
  owner_id   INT REFERENCES petclinic.owners (id)
);
CREATE INDEX IF NOT EXISTS pets_name ON petclinic.pets (name);
CREATE INDEX IF NOT EXISTS pets_owner_id ON petclinic.pets (owner_id);

CREATE TABLE IF NOT EXISTS petclinic.visits (
  id          INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  pet_id      INT REFERENCES petclinic.pets (id),
  visit_date  DATE,
  description TEXT
);
CREATE INDEX IF NOT EXISTS visits_pet_id ON petclinic.visits (pet_id);
